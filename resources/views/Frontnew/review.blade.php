                     <h2>REVIEW PROCESS</h2>

  <p>All submissions to International Journal For Latest  Engineering Technology & Sciences (IJLETS) are subject to independent review.</p>



 
<h3>We have adopted two methods of paper review</h3>
<b>1. Blind Peer review by expert peer reviewers.</b><br>
<p>
We send the original, unpublished article for review by expert in the related field. Our editorial take decision after the review is complete. This review is adopted in case of technical new research paper.</p>
<b>2. Editorial Board Review</b><br/>
<p>
This technique of review is adopted when the paper is of literature review nature. The research work conducted by graduate, post graduate students and PhD Scholars are assessed through this technique.
</p>
<h3>The objectives of the peer review process include:</h3>
<ul class="list">
<li>To assess the quality of the paper before publication. This reviews decides whether a paper is fit for publication or not.</li>
<li>To provide expert assessment of the quality and soundness of the submission under review</li>
<li>To provide guidance to the editorial decision-maker in making an informed, fair, and objective decision on whether to accept, accept subject to changes, or reject.</li>
<li>To provide guidance to authors on improvements to their submission.</li>
</ul>
<b>Sources of Peer Reviewers</b><br>
<p>
Reviewers are drawn from the journalâ€™s editorial board and volunteer peer reviewer pool.  Occasionally reviewers identified as being suitable through different sources will be approached.</p>
<b>Note : </b>Recommendations from authors of peer reviewers from other institutions is accepted and the authors need to send us the name, affiliations and official email id and contact no. 
